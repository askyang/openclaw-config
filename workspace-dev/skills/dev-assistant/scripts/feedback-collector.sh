# 函数：分析现有反馈
analyze_existing_feedback() {
    echo -e "${BLUE}📊 分析现有反馈...${NC}"
    
    local feedback_files=$(find "$FEEDBACK_DIR" -name "*.md" -not -name "feedback-template.md" | head -10)
    local analysis_file="$FEEDBACK_DIR/feedback-analysis-$(date +%Y%m%d).md"
    
    cat > "$analysis_file" << EOF
# 用户反馈分析报告 | $(date '+%Y年%m月%d日')

## 分析概览
- **分析时间**：$(date '+%Y-%m-%d %H:%M:%S')
- **反馈文件数**：$(echo "$feedback_files" | wc -l)
- **分析周期**：全部历史反馈

## 📁 反馈文件列表
EOF
    
    if [ -n "$feedback_files" ]; then
        for fb_file in $feedback_files; do
            if [ -f "$fb_file" ]; then
                local fb_name=$(basename "$fb_file")
                local fb_date=$(stat -f "%Sm" -t "%Y-%m-%d" "$fb_file")
                local fb_size=$(stat -f "%z" "$fb_file")
                
                echo "- **${fb_name}**：${fb_date}，$((fb_size/1024))KB" >> "$analysis_file"
                
                # 提取关键信息
                local feedback_type=$(grep -i "反馈类型" "$fb_file" | head -1 | sed 's/.*：//' | tr -d ' ' || echo "未知")
                local experience=$(grep -i "使用体验" "$fb_file" | head -1 | sed 's/.*：//' | tr -d ' ' || echo "未知")
                
                if [ "$feedback_type" != "未知" ] || [ "$experience" != "未知" ]; then
                    echo "  - 类型：${feedback_type}，体验：${experience}" >> "$analysis_file"
                fi
            fi
        done
    else
        echo "- ⚠️ 没有找到用户反馈文件" >> "$analysis_file"
        echo "- 💡 建议：主动收集用户反馈，了解使用体验" >> "$analysis_file"
    fi
    
    # 反馈趋势分析
    cat >> "$analysis_file" << EOF

## 📈 反馈趋势分析

### 常见反馈类型
（根据现有反馈分析）

### 用户满意度
（根据使用体验评价分析）

### 改进优先级
1. **高优先级**：影响核心功能的问题
2. **中优先级**：功能改进建议
3. **低优先级**：体验优化建议

## 💡 行动建议

### 立即行动
1. 回复所有未处理的反馈
2. 修复高优先级问题
3. 制定改进计划

### 短期计划 (1-2周)
1. 实施可行的改进建议
2. 优化用户体验
3. 收集更多反馈

### 长期计划 (1个月)
1. 建立定期反馈收集机制
2. 分析反馈趋势，指导开发
3. 提升用户满意度

## 🎯 成功指标
1. **反馈数量**：每月收集至少 5 条有效反馈
2. **处理速度**：反馈在 3 个工作日内响应
3. **满意度**：用户满意度持续提升
4. **改进率**：每月完成 3-5 个改进项

---

**分析完成时间**：$(date '+%Y-%m-%d %H:%M:%S')

**用心倾听，持续改进！** 👂✨
EOF
    
    echo -e "${GREEN}✅ 反馈分析完成: $analysis_file${NC}"
    
    if [ -n "$feedback_files" ]; then
        echo -e "${YELLOW}📊 发现 $(echo "$feedback_files" | wc -l) 个反馈文件${NC}"
    else
        echo -e "${YELLOW}⚠️  没有找到用户反馈文件${NC}"
    fi
}

